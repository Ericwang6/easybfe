class TestLigand:
    """Test Ligand class."""
    
    def test_ligand_creation(self):
        """Test creating a Ligand object with all required fields."""
        ligand = Ligand(
            name="benzene",
            smiles="c1ccccc1",
        )
        
        assert ligand.name == "benzene"
        assert ligand.smiles == "c1ccccc1"
        assert ligand.mol_block != ""
        assert ligand.auxiliary_files == {}
    
    def test_ligand_creation_with_aux_files(self):
        """Test creating a Ligand object with auxiliary files."""
        ligand = Ligand(
            name="benzene",
            smiles="c1ccccc1",
            mol_block="test mol block",
            source="test.sdf",
            auxiliary_files={"ligand.prmtop": "prmtop content"}
        )
        
        assert ligand.auxiliary_files == {"ligand.prmtop": "prmtop content"}
    
    def test_add_aux_file(self):
        """Test adding auxiliary files to a ligand."""
        ligand = Ligand(
            name="benzene",
            smiles="c1ccccc1",
            mol_block="test mol block",
            source="test.sdf"
        )
        
        ligand.add_aux_file("ligand.prmtop", "prmtop content")
        ligand.add_aux_file("ligand.inpcrd", "inpcrd content")
        
        assert ligand.auxiliary_files == {
            "ligand.prmtop": "prmtop content",
            "ligand.inpcrd": "inpcrd content"
        }
    
    def test_add_aux_file_overwrite(self):
        """Test that adding an auxiliary file with existing name overwrites."""
        ligand = Ligand(
            name="benzene",
            smiles="c1ccccc1",
            mol_block="test mol block",
            source="test.sdf"
        )
        
        ligand.add_aux_file("ligand.prmtop", "old content")
        ligand.add_aux_file("ligand.prmtop", "new content")
        
        assert ligand.auxiliary_files == {"ligand.prmtop": "new content"}


class TestLigandLoader:
    """Test LigandLoader class."""
    
    def test_loader_init(self):
        """Test LigandLoader initialization."""
        loader = LigandLoader()
        assert loader is not None
    
    def test_load_from_sdf_file(self, test_ligand_sdf):
        """Test loading ligands from an SDF file."""
        loader = LigandLoader()
        ligands = loader.load(test_ligand_sdf)
        
        assert len(ligands) == 1
        ligand = ligands[0]
        assert ligand.name == "benzene"
        assert "c1ccccc1" == ligand.smiles  # SMILES may vary in canonicalization
        assert ligand.mol_block != ""  # Should have mol block from SDF
        assert ligand.source == test_ligand_sdf.name
        assert isinstance(ligand.mol_block, str)
        
        # Verify mol_block matches what would be generated by direct loading
        suppl = Chem.SDMolSupplier(str(test_ligand_sdf), removeHs=False)
        mol = next(suppl)
        expected_mol_block = Chem.MolToMolBlock(mol)
        assert ligand.mol_block == expected_mol_block
    
    def test_load_from_sdf_with_name_from_stem(self, test_ligand_sdf):
        """Test loading from SDF with name_from_stem option (only works for list of paths)."""
        loader = LigandLoader()
        # name_from_stem only works when a list of paths is provided
        ligands = loader.load([test_ligand_sdf], name_from_stem=True)
        
        assert len(ligands) == 1
        # Name should be "benzene" (either from stem or _Name property)
        assert ligands[0].name == "benzene"
        
        # Verify mol_block matches what would be generated by direct loading
        suppl = Chem.SDMolSupplier(str(test_ligand_sdf), removeHs=False)
        mol = next(suppl)
        expected_mol_block = Chem.MolToMolBlock(mol)
        assert ligands[0].mol_block == expected_mol_block
    
    def test_load_from_sdf_single_path_ignores_name_from_stem(self, test_ligand_sdf):
        """Test that name_from_stem is ignored for single path (uses _Name property)."""
        loader = LigandLoader()
        # Even if name_from_stem=True, single path uses _Name property
        ligands = loader.load(test_ligand_sdf, name_from_stem=True)
        
        assert len(ligands) == 1
        # Should use _Name property from SDF, not filename stem
        assert ligands[0].name == "benzene"  # This might still match, but for different reason
    
    def test_load_from_sdf_only_first(self, temp_dir, test_ligand_sdf, test_ligand_sdf_2):
        """Test loading only first molecule from SDF."""
        loader = LigandLoader()
        
        # Create a multi-molecule SDF file
        suppl1 = Chem.SDMolSupplier(str(test_ligand_sdf), removeHs=False)
        suppl2 = Chem.SDMolSupplier(str(test_ligand_sdf_2), removeHs=False)
        
        multi_sdf = temp_dir / "multi_ligands.sdf"
        with Chem.SDWriter(str(multi_sdf)) as writer:
            for mol in suppl1:
                if mol is not None:
                    writer.write(mol)
            for mol in suppl2:
                if mol is not None:
                    writer.write(mol)
        
        # Load all molecules
        ligands_all = loader.load(multi_sdf)
        assert len(ligands_all) >= 1
        
        # Load only first
        ligands_first = loader.load(multi_sdf, only_first=True)
        assert len(ligands_first) == 1
    
