    def test_load_from_csv_skips_empty_rows(self, temp_dir):
        """Test that empty rows in CSV are skipped."""
        csv_file = temp_dir / "ligands.csv"
        df = pd.DataFrame({
            "Name": ["benzene", "toluene"],
            "SMILES": ["c1ccccc1", "Cc1ccccc1"]
        })
        # Add rows with NaN values (which is what empty strings become in CSV)
        import numpy as np
        df.loc[len(df)] = [np.nan, "invalid"]  # Row with empty name
        df.loc[len(df)] = ["  ", np.nan]  # Row with empty SMILES
        
        df.to_csv(csv_file, index=False)
        
        loader = LigandLoader()
        ligands = loader.load(csv_file, name_col="Name", smi_col="SMILES")
        
        # Should only load benzene and toluene, skip empty rows
        assert len(ligands) == 2
        assert ligands[0].name == "benzene"
        assert ligands[1].name == "toluene"
    
