    def test_load_from_rdkit_molecules(self):
        """Test loading ligands from a list of RDKit molecules."""
        mol1 = Chem.MolFromSmiles("c1ccccc1")
        mol1.SetProp("_Name", "benzene")
        mol2 = Chem.MolFromSmiles("Cc1ccccc1")
        mol2.SetProp("_Name", "toluene")
        
        loader = LigandLoader()
        ligands = loader.load([mol1, mol2])
        
        assert len(ligands) == 2
        assert ligands[0].name == "benzene"
        assert ligands[1].name == "toluene"
        # Source is not set for RDKit molecules in the current implementation
        assert isinstance(ligands[0].mol_block, str)
        assert ligands[0].mol_block != ""  # MolToMolBlock generates mol block even without 3D coords
    
    def test_load_from_rdkit_molecules_with_3d(self):
        """Test loading from RDKit molecules with 3D coordinates."""
        from rdkit.Chem import AllChem
        
        mol = Chem.MolFromSmiles("c1ccccc1")
        mol = Chem.AddHs(mol)
        AllChem.EmbedMolecule(mol)
        AllChem.MMFFOptimizeMolecule(mol)
        mol.SetProp("_Name", "benzene")
        
        loader = LigandLoader()
        ligands = loader.load([mol])
        
        assert len(ligands) == 1
        assert ligands[0].name == "benzene"
        # Should have mol_block if 3D coordinates exist
        assert isinstance(ligands[0].mol_block, str)
    
